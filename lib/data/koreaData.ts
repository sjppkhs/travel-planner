export function searchSpots(region: string): TravelSpot[] {
  const normalized = region.trim();
  return spots.filter(
    (s) => s.region === normalized || s.region.includes(normalized) || normalized.includes(s.region),
  );
}

export function getRegionSuggestions(query: string): { name: string; province: string; tags?: string[] }[] {
  const q = query.trim();
  if (!q) return ALL_REGIONS.slice(0, 12);
  return ALL_REGIONS.filter(
    (r) => r.name.includes(q) || r.province.includes(q) || (r.tags ?? []).some((t) => t.includes(q)),
  ).slice(0, 15);
}
